import math

pi = 3.14
minpi = -3.14
x = 1
y = 2
z = 3


print(round(pi))
# round up
print(math.ceil(pi))
# round down
print(math.floor(pi))
# absolute value
print(abs(minpi))
# raise a number to a power
print(pow(pi, 2))
# square root
print(math.sqrt(pi))
# max - find the greatest value of multiple values
print(max(x,y,z))
# min - find the lowest value
print(min(x,y,z))